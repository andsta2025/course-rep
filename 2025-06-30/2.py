def sting_in_reverse_order(s):
    """ function that returns the string in reverse order """
    return s[::-1]

print(sting_in_reverse_order("Hello, Hello!"))
print(sting_in_reverse_order("Python or nohtyP"))
print(sting_in_reverse_order("1234567890"))
print(sting_in_reverse_order("satreblA"))
print(sting_in_reverse_order("alus"))

